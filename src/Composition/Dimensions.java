package Composition;


public class Dimensions {
    private int sidth;
    private int height;
    private int depth;

    public Dimensions(int sidth, int height, int depth) {
        this.sidth = sidth;
        this.height = height;
        this.depth = depth;
    }

    public int getSidth() {
        return sidth;
    }

    public int getHeight() {
        return height;
    }

    public int getDepth() {
        return depth;
    }
}
